package curso.java.PrimeiraTarefaJava;

public class PrimeiraTarefaJava {
    public static void main(String[] args) {

        int valor = 10;
        int quantidade = 20;
        int total = valor * quantidade;

        String msgValor = "valor: " + valor;
        String msgQuantidade = "quantidade: " + quantidade;
        String msgTotal = "total: " + total;

        System.out.println(msgValor+"\n"+msgQuantidade+"\n"+msgTotal);

    }
}
